
                    "Glossary"

  (def computer-language
    "a program that interprets human-controlled
     input of unbounded complexity")

  (def intermediate-representation ^{:abbr "IR"}
    "a human-representable datastructure
     generated from user input")

  (def parser
    "a program that turns text into
     data structures")

  (def code
    "input to a computer language")

