
                        "Glossary"

  (def computer-language
    "a program that interprets human-controlled input
     of unbounded complexity")

  (def IR ^{:short-for "intermediate representation"}
    "a human-representable datastructure generated from user input")

  (def parser
    "a program that turns text into data structures")

  (def bytecode
    "an IR that can be interpreted")

  (def code
    "input to a computer language")

